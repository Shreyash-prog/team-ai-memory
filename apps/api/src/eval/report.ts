import type { IR } from '@team-ai-memory/shared';

export interface EvalUsage {
  inputTokens: number;
  outputTokens: number;
  cacheReadInputTokens: number;
  cacheCreationInputTokens: number;
}

export interface EvalResult {
  fixtureFile: string;
  name: string;
  description: string;
  turns: number;
  latencyMs: number;
  usage: EvalUsage;
  costUsd: number;
  /** The extracted IR, or null if extraction/validation failed. */
  ir: IR | null;
  /** Present when extraction failed. */
  error?: string;
}

export interface ReportData {
  generatedAt: string;
  model: string;
  results: EvalResult[];
}

function money(usd: number): string {
  return `$${usd.toFixed(4)}`;
}

/**
 * Render the collected eval results to markdown.
 *
 * `assessments` maps a fixtureFile to the author's qualitative take. Any fixture
 * without an entry renders a `_(pending)_` placeholder so a freshly-run report
 * is still well-formed before assessments are written.
 */
export function renderReport(data: ReportData, assessments: Record<string, string>): string {
  const { results } = data;
  const totalLatency = results.reduce((s, r) => s + r.latencyMs, 0);
  const totalCost = results.reduce((s, r) => s + r.costUsd, 0);
  const failures = results.filter((r) => r.ir === null);

  const lines: string[] = [];

  lines.push('# Extraction eval — baseline report');
  lines.push('');
  lines.push(
    `Generated by \`pnpm -F api run eval\` on ${data.generatedAt} using model \`${data.model}\`.`
  );
  lines.push('');
  lines.push(
    'Fixtures are **synthetic** (not real-anonymized chats) — see the per-fixture `description` for the stress pattern each one exercises. Each result below uses the production extraction prompt and tool definition (`getExtractionPrompt` / `getExtractionTool`) via a single call.'
  );
  lines.push('');

  // ----- Summary -----
  lines.push('## Summary');
  lines.push('');
  lines.push(`- **Fixtures run:** ${results.length}`);
  lines.push(`- **Extraction failures:** ${failures.length}${failures.length ? ` (${failures.map((f) => f.fixtureFile).join(', ')})` : ''}`);
  lines.push(`- **Total latency:** ${(totalLatency / 1000).toFixed(1)} s (avg ${Math.round(totalLatency / results.length)} ms/fixture)`);
  lines.push(`- **Total cost:** ${money(totalCost)} (avg ${money(totalCost / results.length)}/fixture)`);
  lines.push('');

  // ----- Per-fixture table -----
  lines.push('| # | Fixture | Turns | Latency (ms) | Cost | Status |');
  lines.push('|---|---|--:|--:|--:|---|');
  results.forEach((r, i) => {
    lines.push(
      `| ${i + 1} | \`${r.fixtureFile}\` | ${r.turns} | ${r.latencyMs} | ${money(r.costUsd)} | ${r.ir ? 'ok' : '**FAILED**'} |`
    );
  });
  lines.push('');

  // ----- Per-fixture detail -----
  results.forEach((r, i) => {
    lines.push(`## ${i + 1}. ${r.name}`);
    lines.push('');
    lines.push(`\`${r.fixtureFile}\` · ${r.turns} turns`);
    lines.push('');
    lines.push(`**Description:** ${r.description}`);
    lines.push('');
    lines.push(
      `**Latency:** ${r.latencyMs} ms · **Cost:** ~${money(r.costUsd)} · **Tokens:** ${r.usage.inputTokens} in / ${r.usage.outputTokens} out`
    );
    lines.push('');

    if (r.ir) {
      lines.push('**Extracted IR:**');
      lines.push('');
      lines.push('```json');
      lines.push(JSON.stringify(r.ir, null, 2));
      lines.push('```');
    } else {
      lines.push(`**Extraction failed:** ${r.error ?? 'unknown error'}`);
    }
    lines.push('');

    const assessment = assessments[r.fixtureFile];
    lines.push(`**Assessment:** ${assessment ?? '_(pending)_'}`);
    lines.push('');
  });

  return lines.join('\n') + '\n';
}
